/// Design system spacing tokens
class AppSpacing {
  AppSpacing._();

  /// 4px
  static const double xs = 4.0;

  /// 8px
  static const double sm = 8.0;

  /// 12px
  static const double md = 12.0;

  /// 16px
  static const double lg = 16.0;

  /// 24px
  static const double xl = 24.0;

  /// 32px
  static const double xxl = 32.0;

  /// 48px
  static const double xxxl = 48.0;

  /// 64px
  static const double xxxxl = 64.0;

  /// Standard padding for cards, containers, etc.
  static const double cardPadding = lg;

  /// Standard padding for screen edges
  static const double screenPadding = lg;

  /// Standard gap between elements
  static const double gap = md;

  /// Small gap between elements
  static const double gapSmall = sm;

  /// Large gap between elements
  static const double gapLarge = xl;
}
